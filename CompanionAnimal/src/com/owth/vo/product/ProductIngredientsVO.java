package com.owth.vo.product;

import com.owth.vo.common.PagingVO;

public class ProductIngredientsVO extends PagingVO {

	public String IngredientsName = "";		// 성분명
	public String useYN = "";				// 성분명 삭제여부
	public String writer = "";				// 등록자
	public String writeDate = "";			// 등록일
	public String modifier = "";			// 수정자
	public String modifyDate = "";			// 수정일

	int IngredientsIdx = 0;					// 성분 Index
	int productIDX = 0;						// 상품코드
	
	/**
	 * @return the ingredientsIdx
	 */
	public int getIngredientsIdx() {
		return IngredientsIdx;
	}
	
	/**
	 * @param ingredientsIdx the ingredientsIdx to set
	 */
	public void setIngredientsIdx(int ingredientsIdx) {
		IngredientsIdx = ingredientsIdx;
	}

	/**
	 * @return the ingredientsName
	 */
	public String getIngredientsName() {
		return IngredientsName;
	}

	/**
	 * @param ingredientsName the ingredientsName to set
	 */
	public void setIngredientsName(String ingredientsName) {
		IngredientsName = ingredientsName;
	}

	/**
	 * @return the useYN
	 */
	public String getUseYN() {
		return useYN;
	}

	/**
	 * @param useYN the useYN to set
	 */
	public void setUseYN(String useYN) {
		this.useYN = useYN;
	}

	/**
	 * @return the writer
	 */
	public String getWriter() {
		return writer;
	}

	/**
	 * @param writer the writer to set
	 */
	public void setWriter(String writer) {
		this.writer = writer;
	}

	/**
	 * @return the writeDate
	 */
	public String getWriteDate() {
		return writeDate;
	}

	/**
	 * @param writeDate the writeDate to set
	 */
	public void setWriteDate(String writeDate) {
		this.writeDate = writeDate;
	}

	/**
	 * @return the modifier
	 */
	public String getModifier() {
		return modifier;
	}

	/**
	 * @param modifier the modifier to set
	 */
	public void setModifier(String modifier) {
		this.modifier = modifier;
	}

	/**
	 * @return the modifyDate
	 */
	public String getModifyDate() {
		return modifyDate;
	}

	/**
	 * @param modifyDate the modifyDate to set
	 */
	public void setModifyDate(String modifyDate) {
		this.modifyDate = modifyDate;
	}

	/**
	 * @return the productIDX
	 */
	public int getProductIDX() {
		return productIDX;
	}

	/**
	 * @param productIDX the productIDX to set
	 */
	public void setProductIDX(int productIDX) {
		this.productIDX = productIDX;
	}

	
	
}
